// verifyBotCommit ensures the bot commit meets all repository requirements
func verifyBotCommit(branch, prNumber string) {
	prefix := fmt.Sprintf("[%s]", branch)
	fmt.Printf("%s 🔍 Verifying the cloud-java-bot autoconfig commit...\n", prefix)

	// 1. Get the SHA of the most recent commit on the PR using the corrected jq filter
	sha, err := runCmd("gh", "pr", "view", prNumber, "--json", "commits", "--jq", ".commits[-1].oid")
	if err != nil || sha == "" {
		fatalError(branch, "Failed to retrieve the latest commit SHA for PR #%s", prNumber)
	}

	// 2. Fetch the list of files modified in this specific commit
	apiEndpoint := fmt.Sprintf("repos/GoogleCloudPlatform/spring-cloud-gcp/commits/%s", sha)
	filesJSON, err := runCmd("gh", "api", apiEndpoint, "--jq", ".files[].filename")
	if err != nil {
		fatalError(branch, "Failed to retrieve files for commit %s: %v", sha, err)
	}

	files := strings.Split(strings.TrimSpace(filesJSON), "\n")

	// CRITERIA 1: Not Empty
	if len(files) == 0 || (len(files) == 1 && files[0] == "") {
		fatalError(branch, "Review Failed: The autoconfig commit is completely empty.")
	}

	hasReadme := false
	for _, file := range files {
		file = strings.TrimSpace(file)
		if file == "" {
			continue
		}

		// CRITERIA 3: All changes must be within spring-cloud-previews
		if !strings.HasPrefix(file, "spring-cloud-previews/") {
			fatalError(branch, "Review Failed: Commit contains changes outside of spring-cloud-previews!\nViolating file: %s", file)
		}

		// CRITERIA 2: README.md must be updated
		if file == "spring-cloud-previews/README.md" {
			hasReadme = true
		}
	}

	if !hasReadme {
		fatalError(branch, "Review Failed: spring-cloud-previews/README.md was not updated in the commit.")
	}

	fmt.Printf("%s ✅ Commit verification passed! README.md is updated and all changes are within spring-cloud-previews.\n", prefix)
}

// approveWorkflowRuns checks for pending workflow runs on a PR and approves them.
func approveWorkflowRuns(branch, prNumber string) {
	prefix := fmt.Sprintf("[%s]", branch)
	fmt.Printf("%s ⏳ Checking for workflows requiring approval on PR #%s...\n", prefix, prNumber)

	// GitHub takes a few seconds to queue workflows for forked PRs. We poll 6 times (30 seconds).
	for i := 0; i < 6; i++ {
		sha, _ := runCmd("gh", "pr", "view", prNumber, "--json", "headRefOid", "--jq", ".headRefOid")
		if sha == "" {
			time.Sleep(5 * time.Second)
			continue
		}

		endpoint := fmt.Sprintf("repos/GoogleCloudPlatform/spring-cloud-gcp/actions/runs?head_sha=%s", sha)
		
		// Broadened the filter to catch different states GitHub might use for awaiting approval
		jqFilter := `[.workflow_runs[] | select(.status == "action_required" or .status == "waiting" or .conclusion == "action_required") | .id] | join(" ")`
		runIDsStr, _ := runCmd("gh", "api", endpoint, "--jq", jqFilter)
		
		runIDsStr = strings.TrimSpace(runIDsStr)
		if runIDsStr != "" {
			runIDs := strings.Split(runIDsStr, " ")
			for _, runID := range runIDs {
				fmt.Printf("%s 🔓 Approving workflow run ID: %s...\n", prefix, runID)
				approveEndpoint := fmt.Sprintf("repos/GoogleCloudPlatform/spring-cloud-gcp/actions/runs/%s/approve", runID)
				runCmd("gh", "api", "--method", "POST", approveEndpoint)
			}
			return // We found and approved them, we can exit the loop
		}
		
		time.Sleep(5 * time.Second)
	}
	fmt.Printf("%s ✅ No workflows are currently awaiting approval.\n", prefix)
}

