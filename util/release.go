// updateReadmeAdoc updates the README.adoc with the latest artifact versions.
func updateReadmeAdoc(step int) {
	fmt.Printf("\n[main] ▶️ STEP %d: Updating README.adoc with latest versions from Maven Central...\n", step)

	// 1. Fetch maven-metadata.xml
	resp, err := http.Get("https://repo.maven.apache.org/maven2/com/google/cloud/spring-cloud-gcp/maven-metadata.xml")
	if err != nil {
		fatalError("main", "Failed to fetch maven-metadata.xml: %v", err)
	}
	defer resp.Body.Close()

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		fatalError("main", "Failed to read maven-metadata.xml: %v", err)
	}

	// 2. Extract the latest versions for major releases
	// Because maven-metadata.xml is ordered chronologically, the last match for a major version is the latest.
	reVersion := regexp.MustCompile(`<version>([0-9]+\.[0-9]+\.[0-9]+)</version>`)
	matches := reVersion.FindAllStringSubmatch(string(body), -1)

	latestVersions := make(map[string]string)
	for _, match := range matches {
		fullVersion := match[1]
		major := strings.Split(fullVersion, ".")[0]
		latestVersions[major] = fullVersion
	}

	// 3. Read README.adoc
	readmePath := "README.adoc"
	content, err := os.ReadFile(readmePath)
	if err != nil {
		fatalError("main", "Failed to read README.adoc: %v", err)
	}
	contentStr := string(content)

	// 4. Update the lines for all supported major versions (including 7)
	for _, major := range []string{"3", "4", "5", "6", "7"} {
		latest := latestVersions[major]
		if latest == "" {
			continue
		}

		// Find lines that contain "Spring Framework on Google Cloud X."
		reLine := regexp.MustCompile(`(?m)^(.*Spring Framework on Google Cloud ` + major + `\..*)$`)
		contentStr = reLine.ReplaceAllStringFunc(contentStr, func(line string) string {
			// Replace all X.Y.Z versions on that specific line with the latest version
			reReplace := regexp.MustCompile(`[0-9]+\.[0-9]+\.[0-9]+`)
			return reReplace.ReplaceAllString(line, latest)
		})
	}

	if string(content) == contentStr {
		fmt.Println("[main] ℹ️ README.adoc is already up to date. No PR needed.")
		return
	}

	// 5. Write the updated README.adoc
	err = os.WriteFile(readmePath, []byte(contentStr), 0644)
	if err != nil {
		fatalError("main", "Failed to write README.adoc: %v", err)
	}

	// 6. Delete the old shell script
	scriptPath := "docs/update_latest_versions_in_readme.sh"
	os.Remove(scriptPath) // It is safe if this fails (e.g., if already deleted)

	// 7. Commit and create the Pull Request
	fmt.Println("[main] 💾 Committing changes and creating PR...")
	branchName := fmt.Sprintf("docs-update-readme-%d", time.Now().Unix())

	runCmd("git", "checkout", "-b", branchName)
	runCmd("git", "add", readmePath)
	runCmd("git", "add", scriptPath) // Stages the deletion of the shell script if it existed
	runCmd("git", "commit", "-m", "docs: update latest versions in README and remove shell script")
	runCmd("git", "push", "-u", "origin", branchName)

	prURL, err := runCmd("gh", "pr", "create",
		"--title", "docs: update latest versions in README",
		"--body", "Automated PR to update versions in README.adoc and remove the deprecated bash script.",
		"--base", "main")

	if err != nil {
		fatalError("main", "Failed to create PR: %v\nRun 'gh pr create' manually.", err)
	}

	fmt.Printf("[main] 🎉 SUCCESS! Created README.adoc PR!\n")
	fmt.Printf("[main] 🔗 PR URL: %s\n", prURL)
}

